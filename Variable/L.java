class L 
{
	static int a;
	static double d;
	static char ch;
	static boolean b;
	static String s; //Non Premitive Datatype
	public static void main(String[] args) 
	{
		System.out.println(a); // 0 as a default value in int
		System.out.println(d); // 0.0 as a default value in double 
		System.out.println(ch); // empty space as a default value in character
		System.out.println(b);	// false as a default value in boolean
		System.out.println(s);	// null as a default value in String (Non-Premitive Data type)
	}
}
